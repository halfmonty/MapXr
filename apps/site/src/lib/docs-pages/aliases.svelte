<h1>Aliases</h1>

<p>
    <strong>Aliases</strong> are named pre-defined actions.
</p>
<p>
    An example of a useful alias might be a custom vibration pattern that you
    want to reuse paired with certain kinds of actions in a macro. An alias will
    allow you to define a name for a sequence of actions and reuse it throughout
    your macros rather than redefining it each time.
</p>

